# Printing in Ruby

# ===================================================================
# PUTS vs PRINT
# ===================================================================

# puts prints a new line after the print
puts "Katie"
puts "Taylor"

# print prints on whatever line without spacing/new line
print "Hello World."
print " This is not a new line,"
print " and neither is this."

